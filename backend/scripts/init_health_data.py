async def init_health_documents(tenant_id: int, site_id: int):
    """初始化医学知识文档"""
    async with AsyncSessionLocal() as db:
        try:
            # 医学知识数据（保持原样，这里用简化版本说明结构）
            medical_data = get_medical_data()

            # 检查是否已有数据
            existing_collections = await crud_collection.list(db, site_id=site_id)
            if existing_collections and len(existing_collections) >= len(
                medical_data["collections"]
            ):
                logger.warning(f"⚠️  站点已有 {len(existing_collections)} 个合集，跳过初始化")
                return

            # 创建合集和文档
            total_collections = 0
            total_documents = 0

            for collection_data in medical_data["collections"]:
                # 检查合集是否已存在
                result = await db.execute(
                    select(Collection).where(
                        Collection.title == collection_data["title"],
                        Collection.site_id == site_id,
                        Collection.tenant_id == tenant_id,
                    )
                )
                existing = result.scalar_one_or_none()

                if existing:
                    logger.debug(f"  合集已存在：{collection_data['title']}")
                    collection = existing
                else:
                    # 创建合集
                    collection_create = CollectionCreate(
                        title=collection_data["title"],
                        tenant_id=tenant_id,
                        site_id=site_id,
                        parent_id=None,
                        order=collection_data["order"],
                    )
                    collection = await crud_collection.create(db, obj_in=collection_create)
                    await db.commit()
                    await db.refresh(collection)
                    total_collections += 1
                    logger.debug(f"  ✅ 创建合集：{collection.title} (ID: {collection.id})")

                # 创建文档
                for doc_data in collection_data["documents"]:
                    # 检查文档是否已存在
                    doc_result = await db.execute(
                        select(Document).where(
                            Document.title == doc_data["title"],
                            Document.site_id == site_id,
                            Document.collection_id == collection.id,
                            Document.tenant_id == tenant_id,
                        )
                    )
                    existing_doc = doc_result.scalar_one_or_none()

                    if existing_doc:
                        logger.debug(f"    文档已存在：{doc_data['title']}")
                        continue

                    # 计算阅读时间
                    reading_time = calculate_reading_time(doc_data["content"] or "")

                    document_create = DocumentCreate(
                        title=doc_data["title"],
                        tenant_id=tenant_id,
                        content=doc_data["content"],
                        summary=doc_data["summary"],
                        site_id=site_id,
                        collection_id=collection.id,
                        category=doc_data.get("category"),
                        author=doc_data.get("author", "医疗科普团队"),
                        status="published",
                        tags=doc_data.get("tags", []),
                        reading_time=reading_time,
                    )
                    document = await crud_document.create(db, obj_in=document_create)
                    await db.commit()
                    await db.refresh(document)
                    total_documents += 1

                    # 更新站点文章计数
                    await crud_site.increment_article_count(db, site_id=site_id)

                    logger.debug(
                        f"    ✅ 创建文档：{document.title} (ID: {document.id}, 阅读时间: {reading_time}分钟)"
                    )

            logger.info(
                f"\n✅ 初始化完成！创建合集数：{total_collections}，创建文档数：{total_documents}"
            )

        except Exception as e:
            await db.rollback()
            logger.error(f"❌ 初始化医学知识文档失败: {e}", exc_info=True)
            raise


